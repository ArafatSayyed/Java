public class DivisionOfDataTypes {
    public static void main(String[] args) {
        double x = 37;
        double y = 6;
        System.out.println(x/y);
        int a = 27;
        int b = 5;
        System.out.println(a/b);

        System.out.println(x/b);
        System.out.println(a/y);
    }
}
